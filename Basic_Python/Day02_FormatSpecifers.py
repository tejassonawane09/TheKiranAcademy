#%d Uses: %d for Decimal (int)
a=10
b=20
print(a+b)
print('Sum Of',a,'And',b,'is:',a+b)
print('Sum Of %d and %d is: %d'%(a,b,a+b))
# 
#%f Uses: %f for Float
a1=10.578
b2=9.5
print(a1+b2)
print('Sum Of',a1,'And',b2,'is:',a1+b2)
print('Sum Of %f and %f is: %f'%(a1,b2,a1+b2))
print('Sum Of %0.2f and %0.2f is: %0.4f'%(a1,b2,a1+b2))

